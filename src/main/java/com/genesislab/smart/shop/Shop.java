package com.genesislab.smart.shop;

import java.util.*;

/**
 * 
 */
public class Shop {

    /**
     * Default constructor
     */
    public Shop() {
    }

    /**
     * 
     */
    private String shopId;

    /**
     * 
     */
    private String shopName;

    /**
     * 
     */
    private String shopBranch;

    /**
     * 
     */
    private String shopAddress;

    /**
     * 
     */
    private String shopTele;

    /**
     * 
     */
    private String shopManager;

	public String getShopId() {
		return shopId;
	}

	public void setShopId(String shopId) {
		this.shopId = shopId;
	}

	public String getShopName() {
		return shopName;
	}

	public void setShopName(String shopName) {
		this.shopName = shopName;
	}

	public String getShopBranch() {
		return shopBranch;
	}

	public void setShopBranch(String shopBranch) {
		this.shopBranch = shopBranch;
	}

	public String getShopAddress() {
		return shopAddress;
	}

	public void setShopAddress(String shopAddress) {
		this.shopAddress = shopAddress;
	}

	public String getShopTele() {
		return shopTele;
	}

	public void setShopTele(String shopTele) {
		this.shopTele = shopTele;
	}

	public String getShopManager() {
		return shopManager;
	}

	public void setShopManager(String shopManager) {
		this.shopManager = shopManager;
	}


}